package com.lsmp.mp;

/**
 * This is a smartphone review model class
 * @author samzi
 *
 */

public class SmartphoneReview {

	//Instances
	private String review;
	private double rating;
	
	public String getReview() {
		return review;
	}
	public void setReview(String review) {
		this.review = review;
	}
	public double getRating() {
		return rating;
	}
	public void setRating(double rating) {
		this.rating = rating;
	}
	
	
}
